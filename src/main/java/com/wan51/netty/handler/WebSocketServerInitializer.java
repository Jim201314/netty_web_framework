package com.wan51.netty.handler;

import io.netty.channel.ChannelInitializer;
import io.netty.channel.ChannelPipeline;
import io.netty.channel.socket.SocketChannel;
import io.netty.handler.codec.http.HttpObjectAggregator;
import io.netty.handler.codec.http.HttpServerCodec;
import io.netty.handler.ssl.SslContext;
import org.springframework.beans.factory.annotation.Autowired;

import java.util.HashMap;

/**
 */
public class WebSocketServerInitializer extends ChannelInitializer<SocketChannel> {


    private final SslContext sslCtx = null;


    HashMap<String, Object> urlHandlerMap;

    public HashMap<String, Object> getUrlHandlerMap() {
        return urlHandlerMap;
    }

    public void setUrlHandlerMap(HashMap<String, Object> urlHandlerMap) {
        this.urlHandlerMap = urlHandlerMap;
    }

    @Override
    public void initChannel(SocketChannel ch) throws Exception {
        ChannelPipeline pipeline = ch.pipeline();
        if (sslCtx != null) {
            pipeline.addLast(sslCtx.newHandler(ch.alloc()));
        }
        pipeline.addLast(new HttpServerCodec());
        pipeline.addLast(new HttpObjectAggregator(65536));
        pipeline.addLast(new WebSocketServerHandler(urlHandlerMap));
    }
}
